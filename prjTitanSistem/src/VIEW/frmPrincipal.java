    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String placa = txtPlaca.getText();
        String modelo = txtModelo.getText();
        Date data_entrada = Date.valueOf(getData());
        Date data_saida = Date.valueOf(getDataPadrao());
        int tempo = Integer.parseInt(txtHoras.getText());
        float valor = Float.parseFloat(txtValor.getText());
        
        MovimentacaoDTO movimentacaoDTO = new MovimentacaoDTO();
        movimentacaoDTO.setPlaca(placa);
        movimentacaoDTO.setModelo(modelo);
        movimentacaoDTO.setData_entrada(data_entrada);
        movimentacaoDTO.setData_saida(data_saida);
        movimentacaoDTO.setTempo(tempo);
        movimentacaoDTO.setValor_pago(valor);
        
        MovimentacaoDAO movimentacaoDAO = new MovimentacaoDAO();
        movimentacaoDAO.cadastrarMovimentacao(movimentacaoDTO);
        
        listaMovimentacao(true);
        
        CarregarCampos(tblMovimentacao.getRowCount()-1);
    }//GEN-LAST:event_btnCadastrarActionPerformed

