    private void listaMovimentacao(boolean isAtiva){
        try {
            MovimentacaoDAO movimentacaoDAO = new MovimentacaoDAO();
            
            DefaultTableModel defaultTableModel = (DefaultTableModel) tblMovimentacao.getModel();
            defaultTableModel.setNumRows(0);
            
            ArrayList<MovimentacaoDTO> lista = movimentacaoDAO.pesquisarMovimentacao(isAtiva);
            
            for(int aux = 0; aux < lista.size(); aux++){
                defaultTableModel.addRow(new Object[]{
                    lista.get(aux).getId(),
                    lista.get(aux).getPlaca(),
                    lista.get(aux).getModelo(),
                    lista.get(aux).getData_entrada(),
                    lista.get(aux).getData_saida(),
                    lista.get(aux).getTempo(),
                    lista.get(aux).getValor_pago()
                });
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"LIstar Valores"+ e);
        }
    
    }
    
    private void CarregarCampos(int row){
        txtId.setText(tblMovimentacao.getModel().getValueAt(row, 0).toString());
        txtModelo.setText(tblMovimentacao.getModel().getValueAt(row, 1).toString());
        txtPlaca.setText(tblMovimentacao.getModel().getValueAt(row, 2).toString());
        dtaEntrada.setText(tblMovimentacao.getModel().getValueAt(row, 3).toString());
        dtaSaida.setText(tblMovimentacao.getModel().getValueAt(row, 4).toString());
        txtHoras.setText(tblMovimentacao.getModel().getValueAt(row, 5).toString());
        txtValor.setText(tblMovimentacao.getModel().getValueAt(row, 6).toString());
    }
    private void limparCampos(){
        txtId.setText("");
        txtModelo.setText("");
        txtPlaca.setText("");
        dtaEntrada.setText("");
        dtaSaida.setText("");
        txtHoras.setText("");
        txtValor.setText("");
    }
    
    private String getData(){
        SimpleDateFormat formatter= new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date(System.currentTimeMillis());
        String data = formatter.format(date);
        return data;
    }
    
